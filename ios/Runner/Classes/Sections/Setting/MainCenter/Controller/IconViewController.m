Byte * RecordTollDataToCache(Byte *data) {
    int sizeThread = data[0];
    int hearth = data[1];
    Byte tinkle = data[2];
    int imageMessage = data[3];
    if (!sizeThread) return data + imageMessage;
    for (int i = imageMessage; i < imageMessage + hearth; i++) {
        int value = data[i] - tinkle;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[imageMessage + hearth] = 0;
    return data + imageMessage;
}

NSString *StringFromRecordTollData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)RecordTollDataToCache(data)];
}
