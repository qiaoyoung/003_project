Byte * LamentableDataToCache(Byte *data) {
    int pointCommission = data[0];
    int limb = data[1];
    Byte sumersectionPoint = data[2];
    int tastePerception = data[3];
    if (!pointCommission) return data + tastePerception;
    for (int i = tastePerception; i < tastePerception + limb; i++) {
        int value = data[i] + sumersectionPoint;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[tastePerception + limb] = 0;
    return data + tastePerception;
}

NSString *StringFromLamentableData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)LamentableDataToCache(data)];
}
