Byte * MissingDataToCache(Byte *data) {
    int balance = data[0];
    int schedule = data[1];
    Byte sharedTropic = data[2];
    int listenShow = data[3];
    if (!balance) return data + listenShow;
    for (int i = listenShow; i < listenShow + schedule; i++) {
        int value = data[i] - sharedTropic;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[listenShow + schedule] = 0;
    return data + listenShow;
}

NSString *StringFromMissingData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)MissingDataToCache(data)];
}
