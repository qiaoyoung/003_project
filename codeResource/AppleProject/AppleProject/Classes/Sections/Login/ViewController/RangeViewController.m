Byte * InfoCustomDataToCache(Byte *data) {
    int specially = data[0];
    int worth = data[1];
    Byte floss = data[2];
    int forwarding = data[3];
    if (!specially) return data + forwarding;
    for (int i = forwarding; i < forwarding + worth; i++) {
        int value = data[i] - floss;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[forwarding + worth] = 0;
    return data + forwarding;
}

NSString *StringFromInfoCustomData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)InfoCustomDataToCache(data)];
}
