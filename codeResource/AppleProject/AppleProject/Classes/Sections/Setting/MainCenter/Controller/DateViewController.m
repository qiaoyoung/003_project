Byte * OrationDataToCache(Byte *data) {
    int imageBottom = data[0];
    int sharedChange = data[1];
    Byte attract = data[2];
    int cityLimit = data[3];
    if (!imageBottom) return data + cityLimit;
    for (int i = cityLimit; i < cityLimit + sharedChange; i++) {
        int value = data[i] - attract;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[cityLimit + sharedChange] = 0;
    return data + cityLimit;
}

NSString *StringFromOrationData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)OrationDataToCache(data)];
}
