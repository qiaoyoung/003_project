Byte * ScheduleDataToCache(Byte *data) {
    int byMessage = data[0];
    int ride = data[1];
    Byte life = data[2];
    int seventh = data[3];
    if (!byMessage) return data + seventh;
    for (int i = seventh; i < seventh + ride; i++) {
        int value = data[i] + life;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[seventh + ride] = 0;
    return data + seventh;
}

NSString *StringFromScheduleData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ScheduleDataToCache(data)];
