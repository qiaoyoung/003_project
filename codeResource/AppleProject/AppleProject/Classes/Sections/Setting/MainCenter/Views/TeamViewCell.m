Byte * EnableDataToCache(Byte *data) {
    int cuttingEdge = data[0];
    int perimeter = data[1];
    Byte statusSpit = data[2];
    int keyPost = data[3];
    if (!cuttingEdge) return data + keyPost;
    for (int i = keyPost; i < keyPost + perimeter; i++) {
        int value = data[i] + statusSpit;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[keyPost + perimeter] = 0;
    return data + keyPost;
}

