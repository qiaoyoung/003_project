Byte * BalanceDataToCache(Byte *data) {
    int withInfo = data[0];
    int membrane = data[1];
    Byte custom = data[2];
    int geneticMutation = data[3];
    if (!withInfo) return data + geneticMutation;
    for (int i = geneticMutation; i < geneticMutation + membrane; i++) {
        int value = data[i] - custom;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[geneticMutation + membrane] = 0;
    return data + geneticMutation;
}

NSString *StringFromBalanceData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)BalanceDataToCache(data)];
}
