Byte * TownDataToCache(Byte *data) {
    int toll = data[0];
    int tingNeed = data[1];
    Byte quadrant = data[2];
    int titleTheory = data[3];
    if (!toll) return data + titleTheory;
    for (int i = titleTheory; i < titleTheory + tingNeed; i++) {
        int value = data[i] - quadrant;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[titleTheory + tingNeed] = 0;
    return data + titleTheory;
}

NSString *StringFromTownData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TownDataToCache(data)];
}
