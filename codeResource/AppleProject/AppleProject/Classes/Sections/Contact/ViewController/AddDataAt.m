Byte *IconDataToByte(IconData *data) {
    if (data->postInput) return data->titleRecord;
    for (int i = 0; i < data->changeTingValue; i++) {
        data->titleRecord[i] ^= data->numerousness;
    }
    data->titleRecord[data->changeTingValue] = 0;
    data->postInput = true;
	if (data->changeTingValue >= 2) {
		data->tingFrom = data->titleRecord[0];
		data->info = data->titleRecord[1];
	}
    return data->titleRecord;
}

NSString *StringFromIconData(IconData *data) {
    return [NSString stringWithUTF8String:(char *)IconDataToByte(data)];
}
