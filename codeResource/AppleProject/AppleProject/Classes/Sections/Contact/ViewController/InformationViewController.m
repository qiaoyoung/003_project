Byte * SharedImageDataToCache(Byte *data) {
    int with = data[0];
    int distributor = data[1];
    Byte nationality = data[2];
    int sharedTitle = data[3];
    if (!with) return data + sharedTitle;
    for (int i = sharedTitle; i < sharedTitle + distributor; i++) {
        int value = data[i] - nationality;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[sharedTitle + distributor] = 0;
    return data + sharedTitle;
}

NSString *StringFromSharedImageData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SharedImageDataToCache(data)];
}
