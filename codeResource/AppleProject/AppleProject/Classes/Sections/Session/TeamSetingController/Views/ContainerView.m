Byte * ScanDataToCache(Byte *data) {
    int attendanceSend = data[0];
    int fileSpecially = data[1];
    Byte healthInsuranceLab = data[2];
    int amblePopular = data[3];
    if (!attendanceSend) return data + amblePopular;
    for (int i = amblePopular; i < amblePopular + fileSpecially; i++) {
        int value = data[i] - healthInsuranceLab;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[amblePopular + fileSpecially] = 0;
    return data + amblePopular;
}

NSString *StringFromScanData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ScanDataToCache(data)];
}
