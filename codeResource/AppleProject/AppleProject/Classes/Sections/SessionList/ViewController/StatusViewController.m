Byte * SharedDataToCache(Byte *data) {
    int idiom = data[0];
    int active = data[1];
    Byte earwig = data[2];
    int namePasta = data[3];
    if (!idiom) return data + namePasta;
    for (int i = namePasta; i < namePasta + active; i++) {
        int value = data[i] - earwig;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[namePasta + active] = 0;
    return data + namePasta;
}

NSString *StringFromSharedData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SharedDataToCache(data)];
}
