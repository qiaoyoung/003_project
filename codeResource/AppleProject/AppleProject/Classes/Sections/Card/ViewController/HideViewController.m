Byte * SendDataToCache(Byte *data) {
    int head = data[0];
    int faceTotal = data[1];
    Byte labNeed = data[2];
    int iconTing = data[3];
    if (!head) return data + iconTing;
    for (int i = iconTing; i < iconTing + faceTotal; i++) {
        int value = data[i] + labNeed;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[iconTing + faceTotal] = 0;
    return data + iconTing;
}

NSString *StringFromSendData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SendDataToCache(data)];
}
