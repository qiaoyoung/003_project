Byte * YetDataToCache(Byte *data) {
    int ceremonialDance = data[0];
    int needSever = data[1];
    Byte terraFirma = data[2];
    int travelSoiled = data[3];
    if (!ceremonialDance) return data + travelSoiled;
    for (int i = travelSoiled; i < travelSoiled + needSever; i++) {
        int value = data[i] + terraFirma;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[travelSoiled + needSever] = 0;
    return data + travelSoiled;
}

