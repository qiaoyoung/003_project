Byte * RefugeDataToCache(Byte *data) {
    int parkDoctrinePork = data[0];
    int fin = data[1];
    Byte legalProceeding = data[2];
    int separateSorry = data[3];
    if (!parkDoctrinePork) return data + separateSorry;
    for (int i = separateSorry; i < separateSorry + fin; i++) {
        int value = data[i] - legalProceeding;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[separateSorry + fin] = 0;
    return data + separateSorry;
}

NSString *StringFromRefugeData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)RefugeDataToCache(data)];
}
